func Crc_Teltonika(buf []byte) uint16 {
	var crc uint16 = 0x0000

	for _, b := range buf {
		crc = uint16(crc>>8) ^ uint16(teltonika_crc_table[((crc)^uint16(b))&0xff])
	}

	return crc
}

