func Crc_Wanway(buf []byte) uint16 {
	var fcs uint16 = 0xffff

	var b16 uint16
	for _, b8 := range buf {
		b16 = uint16(b8)
		crc_tab_idx := (fcs ^ b16) & 0x00ff
		fcs = uint16(fcs>>8) ^ wanway_crc_table[crc_tab_idx]
	}
	return fcs ^ 0xffff

}
